int	ft_sqrt(int nb)
{
	int	sqr;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	sqr = 2;
	while ((sqr * sqr) < nb)
		sqr++;
	if ((sqr * sqr) == nb)
		return (sqr);
	return (0);
}
